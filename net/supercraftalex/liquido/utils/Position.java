package net.supercraftalex.liquido.utils;

public class Position {
	
	public double x;
	public double y;
	public double z;
	
	public Position() {
		this.x = 0;
		this.y = 0;
		this.z = 0;
	}
	public Position(double x_n, double y_n, double z_n) {
		this.x = x_n;
		this.y = y_n;
		this.z = z_n;
	}
	
}
