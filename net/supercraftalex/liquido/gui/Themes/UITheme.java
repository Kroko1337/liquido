package net.supercraftalex.liquido.gui.Themes;

import java.awt.Color;

public class UITheme {
	
	public String name;
	public Color main;
	public Color main2;
	public Color main3;
	public Color main4;
	public Color main5;
	
	public UITheme(String name, Color m, Color m2, Color m3, Color m4, Color m5) {
		this.name = name;
		this.main = m;
		this.main2 = m2;
		this.main3 = m3;
		this.main4 = m4;
		this.main5 = m5;
	}
	
}